import openai
import streamlit as st

# 1. 아까 발급받은 키를 여기에 넣습니다.
api_key = st.secrets["OPENAI_API_KEY"]
client = openai.OpenAI(api_key=api_key)

def ask_gpt(news_text):
    # 2. GPT에게 보낼 메시지 구성 (주문서 작성)
    response = client.chat.completions.create(
        model="gpt-3.5-turbo",  # 사용할 모델 (gpt-4o가 더 똑똑하지만 조금 더 비쌈)
        messages=[
            {
                "role": "system", 
                "content": """
당신은 뉴스 요약 전문가입니다. 
사용자가 입력한 뉴스 본문을 읽고 다음 규칙을 따라주세요:

1. 핵심 내용을 3줄 이내로 요약하세요.
2. 기사의 논조(긍정/부정/중립)를 판단하세요.
3. [중요] 만약 기사가 광고이거나, 본문이 너무 짧거나, 검색어와 관련이 없다면 요약하지 말고 "SKIP"이라고만 출력하세요.
"""
            },
            {
                "role": "user", 
                "content": news_text  # 실제 뉴스 본문이 들어갈 자리
            }
        ]
    )
    
    # 3. 답변 받기 (배달 도착)
    # 복잡한 응답 데이터 중에서 '내용(content)'만 쏙 뽑아냅니다.
    return response.choices[0].message.content

